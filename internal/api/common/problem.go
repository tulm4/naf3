// Package common provides RFC 7807 Problem Details for HTTP APIs.
// Spec: RFC 7807
package common

// ProblemDetails represents an RFC 7807 error response.
// It provides a standardized format for API error responses across
// the NSSAAF service interfaces (N58, N60, N59).
// Spec: RFC 7807, TS 29.526 §7 (error responses)
//
//nolint:govet // field ordering matches RFC 7807 spec: status is last for backwards compatibility
type ProblemDetails struct {
	// Type is a URI reference that identifies the problem type.
	// When dereferenced, it SHOULD provide human-readable documentation.
	Type string `json:"type,omitempty"`
	// Title is a short, human-readable summary of the problem type.
	// It SHOULD NOT change from occurrence to occurrence of the problem.
	Title string `json:"title,omitempty"`
	// Detail is a human-readable explanation specific to this occurrence
	// of the problem.
	Detail string `json:"detail,omitempty"`
	// Instance is a URI reference that identifies the specific occurrence
	// of the problem. It may or may not yield further information if
	// dereferenced.
	Instance string `json:"instance,omitempty"`
	// Cause is a 3GPP-specific error cause identifier that provides
	// additional granularity beyond HTTP status codes.
	// Spec: TS 29.526 §7 (cause values)
	Cause string `json:"cause,omitempty"`
	// Status is the HTTP status code generated by the origin server
	// for this occurrence of the problem.
	Status int `json:"status,omitempty"`
}

// Error implements the error interface so ProblemDetails can be returned
// from functions that return error.
// Spec: RFC 7807 does not require this, but it makes ProblemDetails compatible
// with Go's error handling patterns.
func (p *ProblemDetails) Error() string {
	if p.Title != "" {
		return p.Title + ": " + p.Detail
	}
	return p.Detail
}

// NewProblem creates a ProblemDetails with the required fields.
// Use this for structured error responses that follow RFC 7807.
func NewProblem(status int, cause, detail string) *ProblemDetails {
	return &ProblemDetails{
		Status: status,
		Cause:  cause,
		Detail: detail,
	}
}

// ValidationProblem creates a ProblemDetails for input validation errors (HTTP 400).
// Spec: RFC 7807 §4, TS 29.526 §7 (invalid payload)
func ValidationProblem(field, reason string) *ProblemDetails {
	return &ProblemDetails{
		Type:   "https://nssAAF.operator.com/probs/validation-error",
		Title:  "Input Validation Failed",
		Status: 400,
		Detail: field + ": " + reason,
		Cause:  "VALIDATION_ERROR",
	}
}

